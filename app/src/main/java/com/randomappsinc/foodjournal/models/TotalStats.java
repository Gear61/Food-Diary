package com.randomappsinc.foodjournal.models;

public class TotalStats {

    private int numDishes;
    private int numFavorites;
    private int numRestaurants;
    private int numCheckIns;

    public int getNumDishes() {
        return numDishes;
    }

    public void setNumDishes(int numDishes) {
        this.numDishes = numDishes;
    }

    public int getNumFavorites() {
        return numFavorites;
    }

    public void setNumFavorites(int numFavorites) {
        this.numFavorites = numFavorites;
    }

    public int getNumRestaurants() {
        return numRestaurants;
    }

    public void setNumRestaurants(int numRestaurants) {
        this.numRestaurants = numRestaurants;
    }

    public int getNumCheckIns() {
        return numCheckIns;
    }

    public void setNumCheckIns(int numCheckIns) {
        this.numCheckIns = numCheckIns;
    }
}
